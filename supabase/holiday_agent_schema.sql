-- Message policies (via session ownership)
drop policy if exists "messages_select_own" on holiday_agent_messages;
create policy "messages_select_own" on holiday_agent_messages
for select using (
  exists (
    select 1 from holiday_agent_sessions s
    where s.id = holiday_agent_messages.session_id
      and s.user_id = auth.uid()
  )
);

drop policy if exists "messages_insert_own" on holiday_agent_messages;
create policy "messages_insert_own" on holiday_agent_messages
for insert with check (
  exists (
    select 1 from holiday_agent_sessions s
    where s.id = holiday_agent_messages.session_id
      and s.user_id = auth.uid()
  )
);

